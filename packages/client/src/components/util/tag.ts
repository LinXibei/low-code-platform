function isHtmlTag(name: string) {
  return htmlTags.includes(name)
}
function isTransition(name: string) {
  return ["transition-group", "TransitionGroup"].includes(name)
}
function isHtmlAttribute(name: string) {
  return ["id", "class", "role", "style"].includes(name) || name.startsWith("data-") || name.startsWith("aria-")
}
